-- Железная кирка
minetest.register_tool("misha:iron_pickaxe", {
    -- Как предмет будет называться в игре
    description = "Iron Pickaxe",
    -- Изображение предмета в инвентаре (путь к .png файлу)
    inventory_image = "iron_pickaxe.png",

    -- БЛОК СПОСОБНОСТЕЙ ИНСТРУМЕНТА (самое важное для кирки)
    tool_capabilities = {
        -- Время между ударами в секундах
        full_punch_interval = 1.0,
        -- Максимальный уровень блока, после которого не будет выпадать ресурс
        max_drop_level = 1,
        -- ГРУППЫ, КОТОРЫЕ КИРКА МОЖЕТ КОПАТЬ
        groupcaps = {
            -- "cracky" — это группа для камней и руд (то, для чего нужна кирка)
            cracky = {
                -- Время в секундах на копку блоков разного уровня сложности:
                -- [уровень_сложности] = время_в_секундах
                times = { [1] = 2.5, [2] = 1.5, [3] = 0.8 },
                -- Прочность: сколько блоков сломает до поломки (см. примечание ниже)
                uses = 180,
                -- Максимальный уровень блока, который может копать эта кирка
                maxlevel = 2,
            },
        },
        -- Урон по существам (группа "fleshy" — это мобы и игроки)
        damage_groups = { fleshy = 6 },
    },
    -- Группы предмета (например, для модов, которые ищут кирки)
    groups = { pickaxe = 1, tool = 1 },
})

